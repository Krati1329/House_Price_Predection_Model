# PREDICTION FUNCTION
def predict_price(total_sqft, bath, bhk, location_name):
    try:
        location_code = label_encoder.transform([location_name])[0]
    except:
        location_code = -1

    input_df = pd.DataFrame([{
        'total_sqft': total_sqft,
        'bath': bath,
        'BHK': bhk,
        'location_encoded': location_code
    }])

    return round(gbr_model.predict(input_df)[0], 2)

