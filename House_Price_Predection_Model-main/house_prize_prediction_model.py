def clean_total_sqft(x):
    try:
        return float(x)
    except:
        try:
            # Range "1200-1500"
            if '-' in str(x):
                tokens = x.split('-')
                return (float(tokens[0]) + float(tokens[1])) / 2
            # Area format "30x40"
            elif 'x' in str(x).lower():
                sides = str(x).lower().split('x')
                return float(sides[0]) * float(sides[1])
            # Acres to sqft
            elif 'acre' in str(x).lower():
                num = float(str(x).lower().replace('acres','').replace('acre',''))
                return num * 43560
        except:
            return None

